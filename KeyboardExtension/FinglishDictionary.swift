    private func addWords(_ words: [(String, [String], Int)]) {
        for (key, values, frequency) in words {
            if wordMap[key] != nil {
                wordMap[key]?.append(contentsOf: values)
            } else {
                wordMap[key] = values
            }
            for value in values {
                frequencyMap[value] = max(frequencyMap[value] ?? 0, frequency)
            }
        }
    }

